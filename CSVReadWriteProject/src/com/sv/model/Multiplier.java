package com.sv.model;

public class Multiplier {

	private String multiplierAW;
	private String multiplierAX;

	public Multiplier(String multiplierAW, String multiplierAX) {
		super();
		this.multiplierAW = multiplierAW;
		this.multiplierAX = multiplierAX;
	}

	public String getMultiplierAW() {
		return multiplierAW;
	}

	public void setMultiplierAW(String multiplierAW) {
		this.multiplierAW = multiplierAW;
	}

	public String getMultiplierAX() {
		return multiplierAX;
	}

	public void setMultiplierAX(String multiplierAX) {
		this.multiplierAX = multiplierAX;
	}
	
}
