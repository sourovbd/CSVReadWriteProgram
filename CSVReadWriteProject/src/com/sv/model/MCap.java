package com.sv.model;

public class MCap {

	private String mCapB;
	private String mCapAW;
	private String mCapAX;
	private String mCapAY;
	
	public MCap(String mCapB, String mCapAW, String mCapAX, String mCapAY) {
		super();
		this.mCapB = mCapB;
		this.mCapAW = mCapAW;
		this.mCapAX = mCapAX;
		this.mCapAY = mCapAY;
	}
	
	public String getmCapB() {
		return mCapB;
	}
	public void setmCapB(String mCapB) {
		this.mCapB = mCapB;
	}
	public String getmCapAW() {
		return mCapAW;
	}
	public void setmCapAW(String mCapAW) {
		this.mCapAW = mCapAW;
	}
	public String getmCapAX() {
		return mCapAX;
	}
	public void setmCapAX(String mCapAX) {
		this.mCapAX = mCapAX;
	}
	public String getmCapAY() {
		return mCapAY;
	}
	public void setmCapAY(String mCapAY) {
		this.mCapAY = mCapAY;
	}
}
