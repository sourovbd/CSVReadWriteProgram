package com.sv.model;

public class AdjDiv {

	private String adjDivB;
	private String adjDivAW;
	private String adjDivAX;
	
	public AdjDiv(String adjDivB, String adjDivAW, String adjDivAX) {
		super();
		this.adjDivB = adjDivB;
		this.adjDivAW = adjDivAW;
		this.adjDivAX = adjDivAX;
	}
	public String getAdjDivAW() {
		return adjDivAW;
	}
	public void setAdjDivAW(String adjDivAW) {
		this.adjDivAW = adjDivAW;
	}
	public String getAdjDivB() {
		return adjDivB;
	}
	public void setAdjDivB(String adjDivB) {
		this.adjDivB = adjDivB;
	}
	public String getAdjDivAX() {
		return adjDivAX;
	}
	public void setAdjDivAX(String adjDivAX) {
		this.adjDivAX = adjDivAX;
	}
}
