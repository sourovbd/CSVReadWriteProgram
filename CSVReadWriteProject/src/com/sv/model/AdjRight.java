package com.sv.model;

public class AdjRight {

	private String AdjRightB;
	private String adjRightAW;
	private String AdjRightAX;
	
	public AdjRight(String adjRightB, String adjRightAW,  String adjRightAX) {
		super();
		this.AdjRightB = adjRightB;
		this.adjRightAW = adjRightAW;
		this.AdjRightAX = adjRightAX;
	}
	
	public String getAdjRightB() {
		return AdjRightB;
	}
	public void setAdjRightB(String adjRightB) {
		this.AdjRightB = adjRightB;
	}
	public String getAdjRightAW() {
		return adjRightAW;
	}
	public void setAdjRightAW(String adjRightAW) {
		this.adjRightAW = adjRightAW;
	}
	
	public String getAdjRightAX() {
		return AdjRightAX;
	}
	public void setAdjRightAX(String adjRightAX) {
		this.AdjRightAX = adjRightAX;
	}
}
